package data;

public class PrintData {

	public static void main(String[] args) {
		// 데이터 다루기 - 코드의 끝은 세미콜론(;)으로 끝남
		// 숫자(상수 - 정해진 수, Constant Number)
		
		System.out.println(10);  //정수
		System.out.println(2.54); //실수
		System.out.println(10 + 2.54);

		System.out.println("============================");

		// 문자 - 쌍따옴표로 감싼 데이터(String)
		System.out.println('A'); // 한 문자인 경우에는 홑따옴표로 감쌈(Char)
		System.out.println('가'); // 한글 한 문자
		System.out.println("apple"); // 문자열
		System.out.println("사과");

		
	}

}
